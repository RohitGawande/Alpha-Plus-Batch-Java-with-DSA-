public class Basic {
    public static void main(String[] args) {
        //Assignment Operator
        /*=  += -= *= /= */
        int a=6;
        int b=2;
        a+=2;
        System.out.println(a);
        a-=3;
        System.out.println(a);
        a/=b;
        System.out.println(a);
        a*=b;
        System.out.println(a);
    }
}
