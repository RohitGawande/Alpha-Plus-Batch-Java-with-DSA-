public class Switch {
    public static void main(String[] args) {
        int num=4;
        switch (num) {
            case 1:System.out.println("First");
                
                break;
            case 2:System.out.println("Second");
                
                 break;
            case 3:System.out.println("Third");
                
                break;
            case 4:System.out.println("Fourth");
                
                break;    
            default:System.out.println("None");
                break;
        }
    }
}
