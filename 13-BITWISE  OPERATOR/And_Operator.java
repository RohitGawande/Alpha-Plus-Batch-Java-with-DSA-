public class And_Operator {
    public static void main(String[] args) {
        //Bitwise  And 
        System.out.println(5 & 6);
        //Bitwise or
        System.out.println(5|6);
        //Bitwise XOR (Diff bits XOR 1 otherwise 0)
        System.out.println(5 ^ 6);
        //Binary one's Complement (Not)
        System.out.println(~5);
    }
}
